SELECT
    "Н_ЛЮДИ"."ИМЯ",
    "Н_ОБУЧЕНИЯ"."НЗК",
    "Н_УЧЕНИКИ"."ГРУППА"
FROM
    "Н_ЛЮДИ"
    RIGHT JOIN "Н_ОБУЧЕНИЯ" ON "Н_ОБУЧЕНИЯ"."ЧЛВК_ИД" = "Н_ЛЮДИ"."ИД"
    RIGHT JOIN "Н_УЧЕНИКИ" ON "Н_УЧЕНИКИ"."ЧЛВК_ИД" = "Н_ЛЮДИ"."ИД"
WHERE
    "Н_ЛЮДИ"."ОТЧЕСТВО" > 'Александрович'
    AND "Н_ОБУЧЕНИЯ"."НЗК" < '999080';