SELECT
    "Н_ТИПЫ_ВЕДОМОСТЕЙ"."НАИМЕНОВАНИЕ",
    "Н_ВЕДОМОСТИ"."ДАТА",
    "Н_ВЕДОМОСТИ"."ЧЛВК_ИД"
FROM
    "Н_ТИПЫ_ВЕДОМОСТЕЙ"
    LEFT JOIN "Н_ВЕДОМОСТИ" ON "Н_ВЕДОМОСТИ"."ТВ_ИД" = "Н_ТИПЫ_ВЕДОМОСТЕЙ"."ИД"
WHERE
    "Н_ТИПЫ_ВЕДОМОСТЕЙ"."ИД" > 1
    AND "Н_ВЕДОМОСТИ"."ЧЛВК_ИД" < 105590
    AND "Н_ВЕДОМОСТИ"."ЧЛВК_ИД" < 153285;

-- 0 строк, т.к. все строки, подподающие под первое условие, содержат "Н_ВЕДОМОСТИ"."ЧЛВК_ИД" > 105590 (значительно)