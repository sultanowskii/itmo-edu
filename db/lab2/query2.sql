SELECT
    "Н_ЛЮДИ"."ИД",
    "Н_ВЕДОМОСТИ"."ЧЛВК_ИД",
    "Н_СЕССИЯ"."УЧГОД"
FROM
    "Н_ЛЮДИ"
    INNER JOIN "Н_ВЕДОМОСТИ" ON "Н_ЛЮДИ"."ИД" = "Н_ВЕДОМОСТИ"."ЧЛВК_ИД"
    INNER JOIN "Н_СЕССИЯ" ON "Н_ВЕДОМОСТИ"."СЭС_ИД" = "Н_СЕССИЯ"."СЭС_ИД"
WHERE
    "Н_ЛЮДИ"."ИМЯ" = 'Николай'
    AND "Н_ВЕДОМОСТИ"."ДАТА" = '1998-01-05'
    AND "Н_СЕССИЯ"."ДАТА" < '2012-01-25';

-- 0 рез-в, т.к. нет сессий до начала 00-ых