SELECT
    "Н_УЧЕНИКИ"."ГРУППА",
    COUNT("Н_УЧЕНИКИ"."ГРУППА") AS "КОЛВО_УЧЕНИКОВ"
FROM
    "Н_УЧЕНИКИ"
    INNER JOIN "Н_ПЛАНЫ" ON "Н_УЧЕНИКИ"."ПЛАН_ИД" = "Н_ПЛАНЫ"."ИД"
    AND "Н_ПЛАНЫ"."УЧЕБНЫЙ_ГОД" = '2010/2011'
    INNER JOIN "Н_ОТДЕЛЫ" ON "Н_ПЛАНЫ"."ОТД_ИД" = "Н_ОТДЕЛЫ"."ИД"
    AND "Н_ОТДЕЛЫ"."КОРОТКОЕ_ИМЯ" = 'КТиУ'
GROUP BY
    "Н_УЧЕНИКИ"."ГРУППА"
HAVING
    COUNT("Н_УЧЕНИКИ"."ГРУППА") < 10
ORDER BY
    "Н_УЧЕНИКИ"."ГРУППА";