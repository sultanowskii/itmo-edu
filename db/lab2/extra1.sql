SELECT
    "Н_ВИДЫ_РАБОТ"."ИД",
    "Н_ВИДЫ_РАБОТ"."НАИМЕНОВАНИЕ",
    STRING_AGG("Н_СВОЙСТВА_ВР"."НАИМЕНОВАНИЕ", ', ') "СВОЙСТВА"
FROM
    "Н_ВИДЫ_РАБОТ"
    LEFT JOIN "Н_ХАРАКТЕРИСТИКИ_ВИДОВ_РАБОТ" ON "Н_ХАРАКТЕРИСТИКИ_ВИДОВ_РАБОТ"."ВР_ИД" = "Н_ВИДЫ_РАБОТ"."ИД"
    LEFT JOIN "Н_СВОЙСТВА_ВР" ON "Н_СВОЙСТВА_ВР"."ИД" = "Н_ХАРАКТЕРИСТИКИ_ВИДОВ_РАБОТ"."СВР_ИД"
GROUP BY
    "Н_ВИДЫ_РАБОТ"."ИД",
    "Н_ВИДЫ_РАБОТ"."НАИМЕНОВАНИЕ"
ORDER BY
    "Н_ВИДЫ_РАБОТ"."НАИМЕНОВАНИЕ";